# Complete Windows Installer Builder for Kuamini Security Client
# This script:
# 1. Builds the PyInstaller executable
# 2. Creates NSIS installer with autostart and systray
# 3. Registers with console automatically
# 4. Handles registration token properly

param(
    [string]$RegistrationToken,
    [string]$ApiBase = "https://kuaminisystems.com/api/agent",
    [string]$ConsoleUrl = "https://kuaminisystems.com/securityAgent",
    [string]$Version = "1.0.0"
)

$ErrorActionPreference = "Stop"

# Get directories
$ScriptDir = $PSScriptRoot
$AgentDir = Split-Path $ScriptDir
$RootDir = Split-Path $AgentDir
$DistDir = Join-Path $AgentDir "dist"
$BuildDir = Join-Path $AgentDir "build"

Write-Host "═══════════════════════════════════════════════════════════" -ForegroundColor Cyan
Write-Host "   Kuamini Security Client - Complete Installer Builder    " -ForegroundColor Cyan
Write-Host "═══════════════════════════════════════════════════════════" -ForegroundColor Cyan
Write-Host ""

# Step 1: Check Python and dependencies
Write-Host "[1/7] Checking Python environment..." -ForegroundColor Yellow
try {
    $pythonVersion = python --version 2>&1
    Write-Host "  ✓ Found: $pythonVersion" -ForegroundColor Green
} catch {
    Write-Host "  ✗ Python not found. Please install Python 3.9+" -ForegroundColor Red
    exit 1
}

# Install dependencies if needed
Write-Host "  Installing/updating dependencies..." -ForegroundColor Gray
python -m pip install --quiet --upgrade pip pyinstaller pillow pystray psutil requests
if ($LASTEXITCODE -ne 0) {
    Write-Host "  ✗ Failed to install dependencies" -ForegroundColor Red
    exit 1
}
Write-Host "  ✓ Dependencies ready" -ForegroundColor Green
Write-Host ""

# Step 2: Generate config.json with registration token
Write-Host "[2/7] Generating configuration..." -ForegroundColor Yellow
$configPath = Join-Path $AgentDir "config.json"
$agentId = [System.Guid]::NewGuid().ToString()

$config = @{
    api_base = $ApiBase
    console_url = $ConsoleUrl
    agent_id = $agentId
    heartbeat_interval = 60
    auto_register = $true
}

if ($RegistrationToken) {
    $config.registration_token = $RegistrationToken
    Write-Host "  ✓ Registration token configured" -ForegroundColor Green
} else {
    Write-Host "  ⚠ No registration token provided (agent will need manual registration)" -ForegroundColor Yellow
}

$config | ConvertTo-Json -Depth 10 | Out-File -FilePath $configPath -Encoding UTF8 -Force
Write-Host "  ✓ Config saved: $configPath" -ForegroundColor Green
Write-Host "  Agent ID: $agentId" -ForegroundColor Gray
Write-Host ""

# Step 3: Build PyInstaller executable
Write-Host "[3/7] Building agent executable..." -ForegroundColor Yellow
$specFile = Join-Path $AgentDir "KuaminiSecurityClient-win.spec"
if (-not (Test-Path $specFile)) {
    Write-Host "  ✗ Spec file not found: $specFile" -ForegroundColor Red
    exit 1
}

# Clean previous build
if (Test-Path (Join-Path $DistDir "KuaminiSecurityClient")) {
    Remove-Item (Join-Path $DistDir "KuaminiSecurityClient") -Recurse -Force -ErrorAction SilentlyContinue
}
if (Test-Path (Join-Path $BuildDir "KuaminiSecurityClient")) {
    Remove-Item (Join-Path $BuildDir "KuaminiSecurityClient") -Recurse -Force -ErrorAction SilentlyContinue
}

Push-Location $AgentDir
try {
    Write-Host "  Running PyInstaller..." -ForegroundColor Gray
    python -m PyInstaller --clean $specFile 2>&1 | Out-Null
    if ($LASTEXITCODE -ne 0) {
        Write-Host "  ✗ PyInstaller failed" -ForegroundColor Red
        exit 1
    }
} finally {
    Pop-Location
}

$exePath = Join-Path $DistDir "KuaminiSecurityClient\KuaminiSecurityClient.exe"
if (-not (Test-Path $exePath)) {
    Write-Host "  ✗ Build failed: executable not found" -ForegroundColor Red
    exit 1
}

$exeSize = [math]::Round((Get-Item $exePath).Length / 1MB, 2)
Write-Host "  ✓ Executable built: $exeSize MB" -ForegroundColor Green
Write-Host ""

# Step 4: Copy config.json to dist folder
Write-Host "[4/7] Packaging configuration..." -ForegroundColor Yellow
Copy-Item $configPath (Join-Path $DistDir "KuaminiSecurityClient\config.json") -Force
Write-Host "  ✓ Config bundled with executable" -ForegroundColor Green
Write-Host ""

# Step 5: Create NSIS installer script
Write-Host "[5/7] Generating installer script..." -ForegroundColor Yellow
$nsisScript = @"
; Kuamini Security Client Installer
; Auto-generated by create-installer.ps1

!include "MUI2.nsh"
!include "FileFunc.nsh"

; Installer settings
Name "Kuamini Security Client"
OutFile "$RootDir\public\tray\KuaminiSecurityClient-installer.exe"
InstallDir "`$PROGRAMFILES\Kuamini Security Client"
RequestExecutionLevel admin

; Interface settings
!define MUI_ICON "$AgentDir\icon.ico"
!define MUI_HEADERIMAGE
!define MUI_ABORTWARNING

; Pages
!insertmacro MUI_PAGE_WELCOME
!insertmacro MUI_PAGE_DIRECTORY
!insertmacro MUI_PAGE_INSTFILES
!insertmacro MUI_PAGE_FINISH

!insertmacro MUI_UNPAGE_CONFIRM
!insertmacro MUI_UNPAGE_INSTFILES

; Language
!insertmacro MUI_LANGUAGE "English"

; Installer sections
Section "MainSection" SEC01
    SetOutPath "`$INSTDIR"
    
    ; Copy all files from PyInstaller dist
    File /r "$DistDir\KuaminiSecurityClient\*.*"
    
    ; Create uninstaller
    WriteUninstaller "`$INSTDIR\Uninstall.exe"
    
    ; Add to Add/Remove Programs
    WriteRegStr HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient" "DisplayName" "Kuamini Security Client"
    WriteRegStr HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient" "UninstallString" "`$INSTDIR\Uninstall.exe"
    WriteRegStr HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient" "DisplayVersion" "$Version"
    WriteRegStr HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient" "Publisher" "Kuamini Systems"
    WriteRegDWORD HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient" "NoModify" 1
    WriteRegDWORD HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient" "NoRepair" 1
    
    ; Add to startup (HKLM for all users)
    WriteRegStr HKLM "Software\Microsoft\Windows\CurrentVersion\Run" "KuaminiSecurityClient" "`$INSTDIR\KuaminiSecurityClient.exe"
    
    ; Create Start Menu shortcut
    CreateDirectory "`$SMPROGRAMS\Kuamini Security Client"
    CreateShortcut "`$SMPROGRAMS\Kuamini Security Client\Kuamini Security Client.lnk" "`$INSTDIR\KuaminiSecurityClient.exe"
    CreateShortcut "`$SMPROGRAMS\Kuamini Security Client\Uninstall.lnk" "`$INSTDIR\Uninstall.exe"
    
    ; Create Desktop shortcut
    CreateShortcut "`$DESKTOP\Kuamini Security Client.lnk" "`$INSTDIR\KuaminiSecurityClient.exe"
    
    ; Launch the agent immediately (will create systray icon and register)
    Exec '"`$INSTDIR\KuaminiSecurityClient.exe"'
    
SectionEnd

; Uninstaller section
Section "Uninstall"
    ; Kill the process if running
    nsExec::ExecToLog 'taskkill /F /IM KuaminiSecurityClient.exe /T'
    Sleep 1000
    
    ; Remove startup entry
    DeleteRegValue HKLM "Software\Microsoft\Windows\CurrentVersion\Run" "KuaminiSecurityClient"
    
    ; Remove from Add/Remove Programs
    DeleteRegKey HKLM "Software\Microsoft\Windows\CurrentVersion\Uninstall\KuaminiSecurityClient"
    
    ; Remove shortcuts
    Delete "`$SMPROGRAMS\Kuamini Security Client\*.*"
    RMDir "`$SMPROGRAMS\Kuamini Security Client"
    Delete "`$DESKTOP\Kuamini Security Client.lnk"
    
    ; Remove files and directories
    RMDir /r "`$INSTDIR"
SectionEnd
"@

$nsisScriptPath = Join-Path $ScriptDir "installer.nsi"
$nsisScript | Out-File -FilePath $nsisScriptPath -Encoding UTF8 -Force
Write-Host "  ✓ NSIS script created" -ForegroundColor Green
Write-Host ""

# Step 6: Build installer with NSIS
Write-Host "[6/7] Building installer executable..." -ForegroundColor Yellow

# Check if NSIS is installed
$nsisPath = $null
$possiblePaths = @(
    "C:\Program Files (x86)\NSIS\makensis.exe",
    "C:\Program Files\NSIS\makensis.exe"
)

foreach ($path in $possiblePaths) {
    if (Test-Path $path) {
        $nsisPath = $path
        break
    }
}

if (-not $nsisPath) {
    Write-Host "  ⚠ NSIS not found. Falling back to ZIP package..." -ForegroundColor Yellow
    Write-Host ""
    Write-Host "  To create a proper installer:" -ForegroundColor Gray
    Write-Host "  1. Download NSIS from https://nsis.sourceforge.io/Download" -ForegroundColor Gray
    Write-Host "  2. Install NSIS" -ForegroundColor Gray
    Write-Host "  3. Run this script again" -ForegroundColor Gray
    Write-Host ""
    
    # Create ZIP fallback
    $zipPath = Join-Path $RootDir "public\tray\KuaminiSecurityClient-portable.zip"
    $publicTrayDir = Join-Path $RootDir "public\tray"
    
    if (-not (Test-Path $publicTrayDir)) {
        New-Item -ItemType Directory -Path $publicTrayDir -Force | Out-Null
    }
    
    if (Test-Path $zipPath) {
        Remove-Item $zipPath -Force
    }
    
    Compress-Archive -Path (Join-Path $DistDir "KuaminiSecurityClient\*") -DestinationPath $zipPath -Force
    $zipSize = [math]::Round((Get-Item $zipPath).Length / 1MB, 2)
    Write-Host "  ✓ Created portable ZIP: $zipSize MB" -ForegroundColor Green
    Write-Host "  Location: $zipPath" -ForegroundColor Gray
} else {
    Write-Host "  Found NSIS: $nsisPath" -ForegroundColor Gray
    
    # Ensure public/tray directory exists
    $publicTrayDir = Join-Path $RootDir "public\tray"
    if (-not (Test-Path $publicTrayDir)) {
        New-Item -ItemType Directory -Path $publicTrayDir -Force | Out-Null
    }
    
    # Build installer
    & $nsisPath $nsisScriptPath
    if ($LASTEXITCODE -ne 0) {
        Write-Host "  ✗ NSIS build failed" -ForegroundColor Red
        exit 1
    }
    
    $installerPath = Join-Path $publicTrayDir "KuaminiSecurityClient-installer.exe"
    if (Test-Path $installerPath) {
        $installerSize = [math]::Round((Get-Item $installerPath).Length / 1MB, 2)
        Write-Host "  ✓ Installer created: $installerSize MB" -ForegroundColor Green
        Write-Host "  Location: $installerPath" -ForegroundColor Gray
    } else {
        Write-Host "  ✗ Installer file not found" -ForegroundColor Red
        exit 1
    }
}
Write-Host ""

# Step 7: Summary
Write-Host "[7/7] Build Summary" -ForegroundColor Yellow
Write-Host "  ✓ Agent executable built and packaged" -ForegroundColor Green
Write-Host "  ✓ Configuration with agent ID embedded" -ForegroundColor Green
if ($RegistrationToken) {
    Write-Host "  ✓ Registration token configured" -ForegroundColor Green
}
Write-Host "  ✓ Auto-start on Windows boot enabled" -ForegroundColor Green
Write-Host "  ✓ System tray icon will appear on launch" -ForegroundColor Green
Write-Host "  ✓ Auto-registration with console enabled" -ForegroundColor Green
Write-Host ""
Write-Host "═══════════════════════════════════════════════════════════" -ForegroundColor Cyan
Write-Host "                    BUILD COMPLETE!                         " -ForegroundColor Green
Write-Host "═══════════════════════════════════════════════════════════" -ForegroundColor Cyan
Write-Host ""

if ($nsisPath) {
    Write-Host "Installer ready: $publicTrayDir\KuaminiSecurityClient-installer.exe" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "Installation will:" -ForegroundColor White
    Write-Host "  • Install to Program Files" -ForegroundColor Gray
    Write-Host "  • Create system tray icon" -ForegroundColor Gray
    Write-Host "  • Register with console automatically" -ForegroundColor Gray
    Write-Host "  • Start on Windows boot" -ForegroundColor Gray
    Write-Host "  • Create desktop and start menu shortcuts" -ForegroundColor Gray
} else {
    Write-Host "Portable package ready: $publicTrayDir\KuaminiSecurityClient-portable.zip" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "Manual installation:" -ForegroundColor White
    Write-Host "  1. Extract ZIP to desired location" -ForegroundColor Gray
    Write-Host "  2. Run KuaminiSecurityClient.exe" -ForegroundColor Gray
    Write-Host "  3. Add to startup folder for auto-start" -ForegroundColor Gray
}
Write-Host ""
